// Helper function to get medicine by ID
export function getMedicineById(id: string): Medicine | undefined {
  return medicineDatabase.find(m => m.id === id);
}

// Helper function to search medicines
export function searchMedicines(query: string): Medicine[] {
  const q = query.toLowerCase();
  return medicineDatabase.filter(m =>
    m.name.toLowerCase().includes(q) ||
    m.brand.toLowerCase().includes(q) ||
    m.genericName.toLowerCase().includes(q) ||
    m.category.toLowerCase().includes(q) ||
    m.uses.some(u => u.toLowerCase().includes(q))
  );
}

// Get medicines by category
export function getMedicinesByCategory(category: string): Medicine[] {
  return medicineDatabase.filter(m => m.category.toLowerCase().includes(category.toLowerCase()));
}

// Get all unique categories
export function getAllCategories(): string[] {
  return [...new Set(medicineDatabase.map(m => m.category))];
}
