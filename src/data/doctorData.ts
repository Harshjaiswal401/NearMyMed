export function getDoctorById(id: string): Doctor | undefined {
  return doctorDatabase.find(d => d.id === id);
}

export function getDoctorsBySpecialty(specialty: string): Doctor[] {
  return doctorDatabase.filter(d => d.specialty.toLowerCase().includes(specialty.toLowerCase()));
}

export function getAvailableDoctors(): Doctor[] {
  return doctorDatabase.filter(d => d.isAvailableNow);
}

export function getAllSpecialties(): string[] {
  return [...new Set(doctorDatabase.map(d => d.specialty))];
}
